const full_name = "Harindra Masna";

console.log("full_name: ", full_name);

// String.prototype.toLowerCase();
console.log("full_name: ", full_name.toLowerCase());

// String.prototype.toUpperCase();
console.log("full_name: ", full_name.toUpperCase());

// String.prototype.replace();
console.log("full_name: ", full_name.replace("Harindra", "Harindra Narayana")); // return: "Harindra Narayana Masna"

// String.prototype.split();
console.log("full_name: ", full_name.split(" ")); // return: ['Harindra', 'Masna']

// String.prototype.substring();
console.log("full_name: ", full_name.substring(0, 3));

// We can declare any variable with `const`, `let` and `var`
