// ======================== Data Type: STRING ======================== //

const firstName = "Harindra"; // Double Quotes
const lastName = "Masna"; // Single Quotes
const nationality = `Indian`; // Back ticks (This is string Template)

// Writing strings properly
const oneOfThe = 'Indian"s'; // We can use back slash `\` to ignore single or double tics inside string
const oneAmongThe = "Odinschool Student's"; //
const learnings = `Odinschool's Courses: "Data Science" and "Web Development"`; //

// ======================== Data Type: NUMBER ======================== //

const age = 25; // Number
const height = 4.5; // Fractional Number
const phoneNumber = 9199999999; // Long Number
const accountBalance = 10e6; // Exponential Number 10e3 => 10 * 10 * 10 = 1000

// ======================== Data Type: Boolean ======================== //
const eatNonVeg = false; // Means `No`
const eatEgg = true; // Mean `Yes`

// ======================== Data Type: Object ======================== //

// A `Property` is collection of `Data`, with or without `Key` and with `Value`
// An `Object` is collection of `Properties`

// Simple Object Example
const fullName = {
  f_name: "Harindra", // f_name: "Harindra" => Property, Key:  `f_name`, Value: `"Harindra"`
  l_name: "Masna", // l_name: "Masna" => Property, Key:  `l_name`, Value: `"Masna"`
};

// Example for Array type Object
const vehiclesOwned = ["Bicycle", "Car"]; // `vehiclesOwned[0]` returns `"Bicycle"`, `vehiclesOwned[1]` returns `"Car"`

// Other type of objects
const violatedAnyRules = null;
